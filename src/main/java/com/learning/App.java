package com.learning;

/**
 * Простой учебный класс для демонстрации запуска приложения и тестирования.
 *
 * Типы данных и переменные:
 * У каждой переменной есть тип, он задаётся при объявлении и не меняется.
 * Форма: тип имяПеременной = значение;
 *
 * Основные типы:
 * - int — целое число (2, -5, 0)
 * - long — большое целое
 * - double — дробное число (3.14)
 * - boolean — true или false
 * - String — строка в кавычках ("Привет")
 */
public class App {

    /**
     * Складывает два целых числа.
     * Параметры a и b — переменные типа int, результат тоже int.
     *
     * @param a первое число
     * @param b второе число
     * @return сумма a и b
     */
    public static int add(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        // String — тип текста; "Проект успешно запущен!" — значение строки
        System.out.println("Проект успешно запущен!");

        // Объявление переменной: тип int, имя result, значение — результат вызова add(2, 3)
        int result = add(2, 3);
        System.out.println("Пример: 2 + 3 = " + result);
    }
}
